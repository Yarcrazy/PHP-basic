<?php

$rf = [
  'Московская область' => ['Москва', 'Зеленоград', 'Клин'],
  'Ленинградская область' => ['Санкт-Петербург', 'Всеволжск', 'Павловск', 'Кронштадт'],
  'Рязанская область' => ['Рязань', 'Касимов', 'Кораблино'],
];

foreach ($rf as $obl => $cities) {
  echo "{$obl}:\n";
  foreach ($cities as $city) {
    if (strpos($city, 'К') === 0) {
      echo "$city ";
    }
  }
  echo "\n";
}